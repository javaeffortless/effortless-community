package org.effortless.core;

public class SecurityModelException extends BaseException {

	public SecurityModelException () {
		super();
	}
	
	public SecurityModelException (String msg) {
		super(msg);
	}
	
	public SecurityModelException (Throwable cause) {
		super(cause);
	}
	
	public SecurityModelException (String msg, Throwable cause) {
		super(msg, cause);
	}
	
}
