package org.effortless.core;

public class VersionModelException extends ModelException {

	public VersionModelException () {
		super();
	}
	
	public VersionModelException (String msg) {
		super(msg);
	}
	
	public VersionModelException (Throwable cause) {
		super(cause);
	}
	
	public VersionModelException (String msg, Throwable cause) {
		super(msg, cause);
	}
	
}
